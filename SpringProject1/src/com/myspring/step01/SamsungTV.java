package com.myspring.step01;

class SamsungTV  {
	void powerOn() {
		System.out.println("SamsungTV---전원 켠다.");
	}

	void powerOff() {
		System.out.println("SamsungTV---전원 끈다.");
	}

	 void volumeUp() {
		System.out.println("SamsungTV---소리 올리다.");
	}

	void volumeDown() {
		System.out.println("SamsungTV---소리 내리다.");
	}
	

}