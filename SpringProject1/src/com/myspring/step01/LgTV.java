package com.myspring.step01;

class LgTV {
	void turnOn() {
		System.out.println("LgTV---전원 켠다.");
	}

	void turnOff() {
		System.out.println("LgTV---전원 끈다.");
	}

	void soundUp() {
		System.out.println("LgTV---소리 올리다.");
	}

	void soundDown() {
		System.out.println("LgTV---소리 내린다.");
	}
}
